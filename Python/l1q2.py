a = int(input("Enter number 1: "), 2)
b = int(input("Enter number 2: "), 2)

print("Bitwise Operators:")

print("a =", bin(a))
print("b =", bin(b))

print("a & b =", bin(a & b))  
print("a | b =", bin(a | b))  
print("a ^ b =", bin(a ^ b))  
print("~a =", bin(~a))  
print("a << 1 =", bin(a << 1))
print("a >> 1 =", bin(a >> 1))  